<h3>パッケージ</h3>
<h4>Webサイトパッケージの追加</h4>
パッケージの理解のためにpieniのWebサイトのパッケージを組み込んでみましょう。
<pre>
composer require sagittar-org/pieni-website:dev-master
cp vendor/sagittar-org/pieni-website/index.php .
</pre>
<p>
pieniのWebサイトが表示されました。<br>
なぜでしょう。<br>
</p>

